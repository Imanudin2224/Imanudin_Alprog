# operasi panjanglahan +
a = 13
b = 3
hasil = a + b 
print(a, "+", b, "=", hasil)

# pengurangan -
hasil = a - b
print(a, "-", b, "=", hasil)

# perkalian *
hasil = a * b
print(a, "*", b, "=", hasil)

# pembagian /
hasil = a / b
print(a, "/", b, "=", hasil)

# pangkat **
hasil = a ^ b
print(a, "^", b, "=", hasil)

# modulus %
hasil = a % b
print(a, "mod", b, "=", hasil)

# floor division //
hasil = a // b
print(a, "floor", b, "=", hasil)

